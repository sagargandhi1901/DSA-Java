package day4;

public class Loops {
    public static void main(String[] args) {

        // while loop
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }

        System.out.println();

        // for loop
        for (int j = 10; j <= 50; j += 10) {
            System.out.println(j);
        }
    }
}
