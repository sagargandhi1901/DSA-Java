package day1;

public class Operators {
    public static void main(String[] args) {

        // Arithmetic operators
        System.out.println(20 + 56);
        System.out.println(55 / 5);
        System.out.println(24 / 7);
        System.out.println(24.0 / 7.0);
        System.out.println(21 % 4);
        System.out.println(11 * 7);
    }
}
